def get_tasks_(dataset,task,step=None):
    if dataset == "v2":
        tasks = task_v2
    elif dataset == "v2s":
        tasks = task_v2s
    elif dataset == "v2m":
        tasks = task_v2m
    else:
        raise NotImplementedError
    
    if step is None:
        return tasks[task].copy
    
    return tasks[task][step].copy()
def get_task_list():
    return list(task_v2.keys())

def get_task_labels(dataset, name, step):
    if dataset == "v2":
        task_dict = task_v2[name]
    elif dataset == "v2s":
        task_dict = task_v2s[name]
    elif dataset == "v2m":
        task_dict = task_v2m[name]
    else:
        raise NotImplementedError
    assert step in task_dict.keys(), f"You should provide a valid step, [{step} is out of range]"

    labels = list(task_dict[step])
    labels_old = [label for s in range(step) for label in task_dict[s]]
    return labels, labels_old

def get_per_task_classes(dataset, name, step):
    if dataset == "v2":
        task_dict = task_v2[name]
    elif dataset == "v2s":
        task_dict = task_v2s[name]
    elif dataset == "v2m":
        task_dict = task_v2m[name]
    else:
        raise NotImplementedError
    assert step in task_dict.keys(), f"You should provide a valid step, [{step} is out of range]"
    # 因为任务不设置背景类，所以需要+1
    classes = [len(task_dict[s]) for s in range(step+1)]
    return classes

